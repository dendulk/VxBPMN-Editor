package org.flair.bp.v2.ctl.util;

public class BadFormattedCTLException extends Exception {

	private static final long serialVersionUID = 1L;

	public BadFormattedCTLException() {
	}

	public BadFormattedCTLException(String arg0) {
		super(arg0);
	}

	public BadFormattedCTLException(Throwable arg0) {
		super(arg0);
	}

	public BadFormattedCTLException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
